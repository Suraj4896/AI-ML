#arithmatic operators
num1 = 2
num2 = 56

print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num2 % num1)
print(num2 ** num1)

#relational operators

print(num1 == num2)
print(num1 > num2)
print(num1 < num2)
print(num1 >= num2)
print(num2 <= num1)
print(num2 != num1)

#assignment operator
a = 5
a += 2
print(a)

a -= 2
print(a)

a *= 2
print(a)

a /= 2
print(a)

#logical operators
var1 = False
var2 = True

print(not var1)
print(not (5 > 8))

print(var1 and var2)
print(var1 or var2)


